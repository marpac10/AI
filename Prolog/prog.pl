protagonista(X,Y):-
	personaggio(X,Y),importanza(X,Y,1).	
	
spinoff(X,Y):-
	autore(X,Z),autore(Y,Z),not(equal(X,Y)),
	write( 'lo spinoff di '), write(X), write(' risulta essere '),write(Y), nl.
	
crossover(X,Y):-
	personaggio(X,Z),protagonista(Y,Z),not(equal(X,Y)),
	write( 'esiste un episodio crossover tra la serie: '), write(X), write(' e la serie: '),write(Y), nl.

antagonista(X,Y):-
	importanza(X,Y,2),
	write( 'antagonista della serie '), write(X), write(' risulta essere '),write(Y), nl.
	
secondario(X,Y):-
	importanza(X,Y,0),
	write( 'personaggio secondario della serie '), write(X), write(' risulta essere '),write(Y), nl.

relazione(X,Y):-
	personaggio(Z,X),personaggio(Z,Y),amore(X,Y),amore(Y,X),
	write('nella serie '),write(Z),write( ' sussiste una relazione tra '), write(X), write(' e '),write(Y), nl.

friendzone(X,Y):-
	personaggio(Z,X),personaggio(Z,Y),amore(X,Y),not(amore(Y,X)),
	write('nella serie '),write(Z),write( ' sussiste una friendzone tra '), write(Y), write(' e il friendzonato '),write(X), nl.
	
terminata(X,Y):-
	stagioni(X,Y,Z),equal(Y,Z),
	write('la serie '),write(X),write( ' risulta essere terminata con un numero di stagioni pari a  '), write(Y), nl.
	
in_corso(X,Y) :-
	stagioni(X,Y,Z),not(equal(Y,Z)),
	write('la serie '),write(X),write( ' risulta essere in corso. Stagioni uscite '), write(Y), write( ' di '), write(Z), nl.
	
presenza(X,Y,Z):-
	tempo_a_schermo(X,Y,G,H), G-1<Z, Z<H+1,
	write('il personaggio '), write(Y), write(' risulta essere presente nella stagione '), write(Z), write(' poiche il suo arco narrativo va dalla stagione '), write(G), write(' alla stagione '), write(H), nl.
	
rivela_morti(X,Y,Z):-
	morto(X,Z), tempo_a_schermo(X,Z,_H,Y),
	write('nella stagione '),write(Y),write( ' della serie '), write(X), write( ' risulta essere deceduto in atroci circostanze '), write(Z), nl.
	